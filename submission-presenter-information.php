<p align="justify">The following are guidelines for presentations associated with papers at the ICSDE'18 Conference. The presentation should be based on the approved/accepted paper, but may include updates and related additional content. The accepted paper must be presented; otherwise it will not be included in the conference proceedings. Moreover, only listed authors may present a paper. 
</p>
<p align="justify">For a successful and productive conference, all presenters should adhere to the following guidelines:
<ul>
<li style="margin:0 20px 20px 0; vertical-align:top" >
All presentations are to be in English.
</li>
<li style="margin:0 20px 20px 0; vertical-align:top">
You will have 15 minutes for your talk, followed by 2 minutes for Q&A. Please design your slides to fit the time frame and practice to make sure you will not run out of time.
</li>
<li style="margin:0 20px 20px 0; vertical-align:top">
Each illustration should not be crowded by text and graphics. Too much text should be avoided: illustrations should support the presentation; they should not be simply read by the presenter. Graphics help in communications, are more understandable, and point out the basic ideas. Use large fonts so they can be read without a major effort.
</li>
<li style="margin:0 20px 20px 0; vertical-align:top">
Presentation should be clearly structured. Begin with a title slide. The index of the presentation should outline the presentation. The basic problem should be clearly stated, as well as the application area. Background and previous work should be summarized to provide a prospective for the results presented in the paper. The innovative approach should be clearly stated. The application of the approach to the application problem should be described, by pointing out the main features and characteristics, the problems and the solutions. The results should be clearly outlined and evaluated. Appropriate comparison with previous results should be presented. Conclusions should summarize the work performed and point out the main innovation and results. Future work and developments could also be sketched.
</li>
<li style="margin:0 20px 20px 0; vertical-align:top">
Each session room will be set with an LCD projector, a microphone, a laptop/desktop and a laser pointer. Each room will have a Session Coordinator and a Student Volunteer that can help to provide technical support.
</li>
<li style="margin:0 20px 20px 0; vertical-align:top">
Session chairs will ensure that all presentations end on time. The room location and talk times for each presentation will be posted on the technical program website.
</li>
</ul>
</p>

