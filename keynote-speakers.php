<p><strong>Michael Gerndt</strong><img class="alignright wp-image-1673" src="./keynote/gerndt.jpg" alt="" width="25%" ><br>
Professor, Technical University of Munich, Germany<br></p>
<p align="justify">Michael Gerndt received a Ph.D. in Computer Science in 1989 from the University of Bonn. He developed SUPERB the first automatic parallelizer for distributed memory parallel machines. For two years, in 1990 and 1991, he held a postdoc position at the University of Vienna and joined Research Centre Juelich in 1992 where he concentrated on programming and implementation issues of shared virtual memory systems. This research led to his habilitation in 1998 at Technische Universität München (TUM). Since 2000 he is professor for architecture of parallel and distributed systems at TUM. His current research focuses on programming models and tools for scalable parallel architectures. He is leading the development of the automatic performance analysis tools Periscope and of iOMP, an extension of OpenMP for invasive computing. iOMP is a research project in the new Transregional Collaborative Research Center InvasIC (TR 89) funded by the German Science Foundation. In addition he is heading projects on parallel programming languages and their implementation on multicore processors as well as resource management in Cloud environments funded by public and industry sources. Since October 2011 he is the coordinator of the European FP7 project AutoTune on automatic online tuning. Michael Gerndt is the contact person of the Faculty of Informatics for international affairs.</p><br>

			<p><strong>Ahmed Abdelgawad</strong><img class=" wp-image-1667 alignright" src="./keynote/AhmeAbdelgawad.JPG" alt="" width="25%" ><br>
Professor, Central Michigan University, USA<br></p>
<p align="justify">Dr. Ahmed Abdelgawad received his M.S. and Ph.D. degree in Computer Engineering from University of Louisiana at Lafayette in 2007 and 2011 and subsequently joined IBM as a Design Aids & Automation Engineering Professional at Semiconductor Research and Development Center. In Fall 2012 he joined Central Michigan University as a Computer Engineering Assistant Professor. In Fall 2017, Dr. Abdelgawad was early promoted as a Computer Engineering Associate Professor. His area of expertise is distributed computing for Wireless Sensor Network (WSN), Internet of Things (IoT), Structural Health Monitoring (SHM), data fusion techniques for WSN, low power embedded system, video processing, digital signal processing, Robotics, RFID, Localization, VLSI, and FPGA design. He has published two books and more than 65 articles in related journals and conferences. Dr. Abdelgawad served as a reviewer for several conferences and journals, including IEEE WF-IoT, IEEE ISCAS, IEEE SAS, Springer, Elsevier, IEEE Transactions on VLSI, and IEEE Transactions on I&M. He severed in the technical committees of IEEE ISCAS 2007, IEEE ISCAS 2008, and IEEE ICIP 2009 conferences. He served in the administration committee of IEEE SiPS 2011. Dr. Abdelgawad has been appointed as a Track Chair of the International Conference on Cognitive and Sensor Networks (MIC-CSN 2013). He also served in the organizing committee of ICECS2013 and 2015 IEEE ICECS2015. Dr. Abdelgawad is the publicity chair in North America of the IEEE WF-IoT 2016/18 conferences. He is the finance chair of the IEEE ICASSP 2017. He is the TPC Co– Chair of IoT International Innovation Conference 2017, the TPC Co– Chair of Global Internet of Things Summit (GIoTS 2017), and the technical program chair of IEEE MWSCAS 2018. He is currently the IEEE Northeast Michigan section chair and IEEE SPS Internet of Things (IoT) SIG Member. In addition, Dr. Abdelgawad served as a PI and Co-PI for several funded grants from NSF.</p>
