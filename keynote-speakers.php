			<p><strong>Amr El Abbadi </strong><img class=" wp-image-1667 alignright" src="./keynote/amrsmall.jpg" alt="" width="25%" ><br>
Professor, University of California at Santa Barbara, USA<br></p>
<p align="justify">Amr El Abbadi is a Professor of Computer Science Department at the University of California, Santa Barbara. Prof. El Abbadi was Chair of the Computer Science Department at UCSB from 2007 to 2011. He has served as a journal editor for several database journals, including, currently, The VLDB Journal and IEEE Transactions on Computers. He has been Program Chair for multiple database and distributed systems conferences. He has served as a board member of the VLDB Endowment from 2002—2008, and is currently a member of the Executive Committee of the Technical Committee of Data Engineering (TCDE). He has published over 300 articles in databases and distributed systems.</p>
