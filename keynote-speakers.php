			<p><strong>Dr. Sajal K. Das </strong><img class=" wp-image-1667 alignright" src="./keynote/Sajal-IEEE-Award.jpg" alt="" width="25%" ><br>
Professor, Missouri University of Science and Technology at Rolla, USA<br></p>
<p align="justify">Dr. Sajal K. Das, whose academic genealogy includes Thomas Alva Edison, is a Professor of Computer Science and
Daniel St. Clair Endowed Chair at the Missouri University of Science and Technology, Rolla, where he was the Chair
of Computer Science Department during 2013-2017. Prior to 2013, he was a University Distinguished Scholar
Professor of Computer Science and Engineering, and founding director of the Center for Research in Wireless
Mobility and Networking (CReWMaN) at the University of Texas at Arlington. During 2008-2011, Dr. Das served
the National Science Foundation as a Program Director in the Computer and Network Systems Division. His research
interests include wireless and sensor networks, mobile and pervasive computing, smart environments (e.g., smart city,
smart grid, smart healthcare), cyber-physical systems and IoTs, crowdsensing, data analytics, cloud computing, cyber-
security and trustworthiness, biological and social networks, applied graph theory and game theory. He has
contributed significantly to these area, having published over 700 research articles in high quality journals and
conferences, and 51 book chapters. A holder of 5 US patents, Dr. Das directed numerous funded projects totaling over
$16 million and coauthored four books – “Smart Environments: Technology, Protocols, and Applications” (John
Wiley, 2005); “Handbook on Securing Cyber-Physical Critical Infrastructure: Foundations and Challenges” (Morgan
Kaufman, 2012); “Mobile Agents in Distributed Computing and Networking” (Wiley, 2012); and “Principles of
Cyber-Physical Systems: An Interdisciplinary Approach” (Cambridge University Press, 2018). According to DBLP,
Dr. Das is one of the most prolific authors in computer science. His h-index is 82 with 27,000+ citations according to
Google Scholar. He is the founding Editor-in-Chief of Elsevier’s Pervasive and Mobile Computing journal since
2005, and serves as an Associate Editor of several journals including IEEE Transactions on Mobile Computing and
ACM Transactions on Sensor Networks. A founder of IEEE PerCom, WoWMoM, SMARTCOMP and ACM ICDCN
conferences, Dr. Das has served as General and Program Chair of numerous conferences. He is a recipient of 10 Best
Paper Awards in prestigious conferences, and received numerous awards for teaching, mentoring and research
including the IEEE Computer Society’s Technical Achievement award for pioneering contributions to sensor
networks and mobile computing. Dr. Das graduated 41 PhD and 32 MS thesis students. He is an IEEE Fellow.</p>
