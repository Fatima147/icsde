									<br><br>
											<strong><span>About Rabat and nearby Cities<br>
</span></strong></h2>

<p align="justify" >Rabat, Morocco’s capital is located on the Atlantic Ocean and the Mediterranean Sea. Morocco is located on the northwestern tip of Africa and just eight miles off the southern coast of Spain.</p>
<p align="justify">Casablanca is 86 kilometers (53 miles) from Rabat. Casablanca is the largest city of Morocco and it has become the economic and business capital of Morocco. Hassan II Mosque, Medina, Place Mohamed V</p>
<p align="justify">Tangier, the third biggest economy is a fascinating city (birthplace of Ibn Battuta, one of the greatest travelers and explorers in the 14th century. A Virtual Tour of Ibn Battuta can be found here: <a href="http://ibnbattuta.berkeley.edu/">http://ibnbattuta.berkeley.edu/</a>). Tangier is an uncontrolled mix of North Africa, Spain, Portugal and France. Tangier is separated from Spain by the 8 miles of the Strait of Gibraltar. Frequent ferries make the short crossing from Europe each day, and many cruise ships sailing between the Mediterranean and the Atlantic often include Tangier as a port of call. Distance between Rabat and Tangier is 217 kilometers (135 miles). You can reach by train (3 hours ride), grand taxi, car or plane. The cheapest way is train which costs MAD 94 (approx. USD 10).</p>
<p align="justify">Marrakesh (286 km from Rabat), a former imperial city in western Morocco, is a major economic center and home to mosques, palaces, gardens and thriving souks (marketplaces) for traditional textiles, pottery &amp; jewelry. It can be reached by train (4 hours ride), taxi, car or plane.</p>
<p align="justify">Rabat has a long, rich history. It is an enjoyable city with interesting historical and cultural monuments for sightseeing including: Rabat Archaeological Museum, the Hassan Mosque, and the Mausoleum of Mohammed V. Other tourist attractions are nearby including Oudaias Kasbah, Medina, Royal Palace, Salé, Fes, Marrakesh, Tangier, Casablanca and many more.</p>
